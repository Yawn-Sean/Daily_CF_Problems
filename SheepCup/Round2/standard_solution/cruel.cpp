// Let's set a bit and flow!
// I came, I divide, I conquer!

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

	int t;
	cin >> t;

	while (t -- ) {
		int x, y, ans = -1;
		cin >> x >> y;
		for (int i = 2; i <= max(x, y); i ++) {
			if (gcd(i, x) == 1 && gcd(i, y) == 1) {
				ans = i;
				break;
			}
		}
		cout << ans << '\n';
	}

    return 0;
}