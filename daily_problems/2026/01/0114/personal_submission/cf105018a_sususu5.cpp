void solve() {
    int n;
    cin >> n;
    string s;
    cin >> s;
    s += '#';
    for (int i = 0; i < n; i++) s += s[i] ^ 1;
    vector<int> pi(2 * n + 1);
    for (int i = 1; i <= 2 * n; i++) {
        int j = pi[i - 1];
        while (j > 0 && s[i] != s[j]) j = pi[j - 1];
        if (s[i] == s[j]) j++;
        pi[i] = j;
    }
    for (int i = n + 1; i <= 2 * n; i++) {
        cout << pi[i] << ' ';
    }
    cout << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}