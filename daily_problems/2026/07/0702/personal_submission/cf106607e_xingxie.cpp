void solve()
{
    int n;
    cin >> n;
    vpii a(n);
    for (int i = 0;i < n;i++) cin >> a[i].fi >> a[i].se;
    int c1 = 0,c2 = 0,c3 = 0;//y > x,y < x,y = x;
    for (auto& [x,y] : a) 
    {
        if (y > x) c1++;
        else if (y < x) c2++;
        else c3++;
    }
    ll ans = 1LL * c1 * c2 + 1LL * c3 * (c1 + c2) + 1LL * c3 * (c3 - 1) / 2;
    cout << ans << endl;
}

