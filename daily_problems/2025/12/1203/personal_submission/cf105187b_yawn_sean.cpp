namespace atcoder {

template <class T> struct fenwick_tree {
    using U = internal::to_unsigned_t<T>;

  public:
    fenwick_tree() : _n(0) {}
    explicit fenwick_tree(int n) : _n(n), data(n) {}

    void add(int p, T x) {
        assert(0 <= p && p < _n);
        p++;
        while (p <= _n) {
            data[p - 1] += U(x);
            p += p & -p;
        }
    }

    T sum(int l, int r) {
        assert(0 <= l && l <= r && r <= _n);
        return sum(r) - sum(l);
    }

  private:
    int _n;
    std::vector<U> data;

    U sum(int r) {
        U s = 0;
        while (r > 0) {
            s += data[r - 1];
            r -= r & -r;
        }
        return s;
    }
};

}  // namespace atcoder


int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);

	int n;
	cin >> n;

	vector<int> nums(2 * n);
	for (auto &x: nums) cin >> x;

	vector<pair<int, int>> competitions(n);
	for (auto &[x, y]: competitions) cin >> x >> y;

	int M = 5e5 + 100;
	atcoder::fenwick_tree<int> fen(M);

	for (auto &[u, v]: competitions) {
		int x = nums[u], y = nums[v];
		if (x > y) swap(x, y);

		fen.add(x, 1);
		fen.add(min(max(x + 1, y + 1), max(x + 3, y)), 1);
	}

	vector<int> ans(2 * n);

	for (auto &[u, v]: competitions) {
		int x = nums[u], y = nums[v];
		if (x > y) swap(x, y);

		fen.add(x, -1);
		fen.add(min(max(x + 1, y + 1), max(x + 3, y)), -1);

		ans[u] = 2 * n - fen.sum(0, nums[u] + 4);
		ans[v] = 2 * n - fen.sum(0, nums[v] + 4);

		if (nums[u] + 3 >= nums[v]) ans[u] --;
		if (nums[v] + 3 >= nums[u]) ans[v] --;

		fen.add(x, 1);
		fen.add(min(max(x + 1, y + 1), max(x + 3, y)), 1);
	}

	for (auto &x: ans) cout << x << ' ';

	return 0;
}