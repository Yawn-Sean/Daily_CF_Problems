// Let's set a bit and flow!
// I came, I divide, I conquer!

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int t;
    cin >> t;

    while (t --) {
        long long n, m, k;
        cin >> n >> m >> k;

        if (k < max(n, m)) cout << -1 << '\n';
        else if ((n + m) % 2) cout << k - 1 << '\n';
        else if (n % 2 == k % 2) cout << k << '\n';
        else cout << k - 2 << '\n';
    }

    return 0;
}