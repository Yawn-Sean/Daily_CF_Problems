#include <bits/stdc++.h>
#define fastio std::ios::sync_with_stdio(0); std::cin.tie(0); std::cout.tie(0)
#define all(x) x.begin(),x.end()
#define pb push_back
#define i64 long long

void solve(){
    int n;
    std::cin>>n;
    std::vector<Mint> f(n+2);
    f[1]=1,f[2]=1;
    for(int i=2;i<=n;i++) f[i]=f[i-1]+f[i-2];
    Mint res=f[n]/Mint(2).pow(n);
    std::cout<<res<<"\n";
}

signed main(){
    fastio;
    
    int T;
    T=1;
    while(T--) solve();
    
    return 0;
}
