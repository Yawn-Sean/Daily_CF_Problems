struct Comb {
    int n;
    std::vector<Z> _fac;
    std::vector<Z> _invfac;
    std::vector<Z> _inv;
    
    Comb() : n{0}, _fac{1}, _invfac{1}, _inv{0} {}
    Comb(int n) : Comb() {
        init(n);
    }
    
    void init(int m) {
        if (m <= n) return;
        _fac.resize(m + 1);
        _invfac.resize(m + 1);
        _inv.resize(m + 1);
        
        for (int i = n + 1; i <= m; i++) {
            _fac[i] = _fac[i - 1] * i;
        }
        _invfac[m] = _fac[m].inv();
        for (int i = m; i > n; i--) {
            _invfac[i - 1] = _invfac[i] * i;
            _inv[i] = _invfac[i] * _fac[i - 1];
        }
        n = m;
    }
    
    Z fac(int m) {
        if (m > n) init(2 * m);
        return _fac[m];
    }
    Z invfac(int m) {
        if (m > n) init(2 * m);
        return _invfac[m];
    }
    Z inv(int m) {
        if (m > n) init(2 * m);
        return _inv[m];
    }
    Z binom(int n, int m) {
        if (n < m || m < 0) return 0;
        return fac(n) * invfac(m) * invfac(n - m);
    }
} comb;


Z get(vector<int> cnt, const string& s) {
    Z res = 0;
    // 找到大于等于s的个数
    for (int i = 0; i < n; i ++) {
        // 如果选择比这位更大的字母
        int c = 0;
        for (int j = s[i] - 'a' + 1; j < 26; j ++) {
            c += cnt[j];
        }
        Z ans = Z(c) * comb.fac(n - i - 1);
        for (int i = 0; i < 26; i ++) {
            if (!cnt[i]) continue;
            ans *= comb.invfac(cnt[i]);
        }
        res += ans;
        if (cnt[s[i] - 'a'] <= 0) break;
        // 选择相等的字母
        cnt[s[i] - 'a'] --;
        if (i == n - 1) res += 1;
    }
    return res;
}
