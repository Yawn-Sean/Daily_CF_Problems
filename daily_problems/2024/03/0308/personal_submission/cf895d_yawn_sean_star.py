class FenwickTree:
    def __init__(self, nums):
        self.bit = nums[:]
        self.n = len(nums)
        for i in range(self.n):
            if i | (i + 1) < self.n:
                self.bit[i | (i + 1)] += self.bit[i]

    def sum(self, r):
        res = 0
        while r >= 0:
            res += self.bit[r]
            r = (r & (r + 1)) - 1
        return res

    def rsum(self, l, r):
        return self.sum(r) - self.sum(l - 1)

    def add(self, idx, delta):
        while idx < self.n:
            self.bit[idx] += delta
            idx = idx | (idx + 1)

