	for(int i=0;i<=n;i++) f[1][i]=1;
	for(int i=2;i<=k;i++){
		for(int j=1;j<=n;j++) f[i-1][j]+=f[i-1][j-1];
		for(int j=1;j<=n;j++){
			int l=n-j,r=n-1;
			f[i][j]+=f[i-1][r]-(l-1>=0 ? f[i-1][l-1] : Mint(0));
		}
	}
	
