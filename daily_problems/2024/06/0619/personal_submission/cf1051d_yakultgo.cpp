constexpr int P = 998244353;
using Z = MInt<P>;

void solve() {
    int n, k;
    cin >> n >> k;
    vector<vector<vector<Z>>> dp(n + 1, vector<vector<Z>>(k + 1, vector<Z>(4)));
    dp[1][1][0] = 1;
    dp[1][1][3] = 1;
    if (k > 1) {
        dp[1][2][1] = 1;
        dp[1][2][2] = 1;
    }
    for (int i = 2; i <= n; i++) {
        for (int j = 1; j <= k; j++) {
            dp[i][j][0] = dp[i - 1][j][0] + dp[i - 1][j][1] + dp[i - 1][j][2] + dp[i - 1][j - 1][3];
            dp[i][j][3] = dp[i - 1][j - 1][0] + dp[i - 1][j][1] + dp[i - 1][j][2] + dp[i - 1][j][3];
            if (j >= 2) {
                dp[i][j][1] = dp[i - 1][j - 1][0] + dp[i - 1][j][1] + dp[i - 1][j - 1][3] + dp[i - 1][j - 2][2];
                dp[i][j][2] = dp[i - 1][j - 1][0] + dp[i - 1][j][2] + dp[i - 1][j - 1][3] + dp[i - 1][j - 2][1];
            }
        }
    }
    Z ans = dp[n][k][0] + dp[n][k][1] + dp[n][k][2] + dp[n][k][3];
    cout << ans << endl;
}
int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int T = 1;
    while (T--) {
        solve();
    }
    return 0;
}