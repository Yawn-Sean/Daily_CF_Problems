template <typename T>
struct Fenwick {
    int n;
    std::vector<T> a;
    
    Fenwick(int n_ = 0) {
        init(n_);
    }
    
    void init(int n_) {
        n = n_;
        a.assign(n, T{});
    }
    
    void add(int x, const T &v) {
        for (int i = x + 1; i <= n; i += i & -i) {
            a[i - 1] = a[i - 1] + v;
        }
    }
    
    T sum(int x) {
        T ans{};
        for (int i = x; i > 0; i -= i & -i) {
            ans = ans + a[i - 1];
        }
        return ans;
    }
    
    T rangeSum(int l, int r) {
        return sum(r) - sum(l);
    }
    
    int select(const T &k) {
        int x = 0;
        T cur{};
        for (int i = 1 << std::__lg(n); i; i /= 2) {
            if (x + i <= n && cur + a[x + i - 1] <= k) {
                x += i;
                cur = cur + a[x - 1];
            }
        }
        return x;
    }
};

void solve() {
    int n = read();
    int k = read() % n;
    if (k > n / 2) k = n - k;

    int res = 1;
    int cur = 0;

    Fenwick<int> fwk(n * 10);

    for (int i = 1; i <= n; i++) {

        if (cur + k <= n) {
            res += fwk.rangeSum(cur + 1, cur + k) + 1;
        } else {
            res += fwk.rangeSum(cur + 1, n) + fwk.rangeSum(0, cur + k - n) + 1;
        }

        fwk.add(cur, 1);
        cur = (cur + k) % n;
        fwk.add(cur, 1);

        cout << res << ' ';
    }
}

