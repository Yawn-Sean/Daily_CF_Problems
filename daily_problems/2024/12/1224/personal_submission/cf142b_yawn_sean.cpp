// Let's set a bit and flow!
// I came, I divide, I conquer!

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n, m;
    cin >> n >> m;

    if (min(n, m) == 1) cout << n * m;
    else if (min(n, m) == 2) {
        int v = max(n, m);
        cout << v / 4 * 4 + min(v % 4, 2) * 2;
    }
    else cout << (n * m + 1) / 2;

    return 0;
}