n = int(input())
a = input()
x, y = map(int, input().split())
if abs(x) + abs(y) > n or (x + y + n) % 2:
    print(-1)
else:
    dx, dy = [0] * n, [0] * n
    for i in range(n):
        if a[i] == 'R':
            dx[i] = 1
        elif a[i] == 'L':
            dx[i] = -1
        elif a[i] == 'U':
            dy[i] = 1
        else:
            dy[i] = -1
    sx, sy = sum(dx), sum(dy)
    if sx == x and sy == y:
        print(0)
    else:
        l = 0
        res = n
        for r in range(n):# å‡å»å³è¾¹çš„æ­¥æ•°
            sx -= dx[r]
            sy -= dy[r]
            while r - l + 1 >= abs(x - sx) + abs(y - sy):
                sx += dx[l]
                sy += dy[l]
                l += 1
            if l > 0:
                res = min(res, r - l + 1)
        print(res)
